#!/usr/bin/env bash
# Record a PR-ready task: appends pr=<url> and the PR head pr_head=<sha> to
# state/<id>.meta when available, then arms the watcher's merge poll by writing
# state/<id>.check.sh, which prints one line iff the PR is merged (the watcher's
# check contract: output = wake firstmate, silence = keep sleeping).
#
# Host-agnostic: GitHub PRs go through gh (unchanged); Gitea PRs go through the
# Gitea REST API. Both paths are provided by bin/fm-pr-host-lib.sh, so a project
# whose canonical remote is a Gitea server is checked the same way.
# Usage: fm-pr-check.sh <task-id> <pr-url>
set -eu

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
FM_ROOT="${FM_ROOT_OVERRIDE:-$(cd "$SCRIPT_DIR/.." && pwd)}"
FM_HOME="${FM_HOME:-${FM_ROOT_OVERRIDE:-$FM_ROOT}}"
STATE="${FM_STATE_OVERRIDE:-$FM_HOME/state}"
# shellcheck source=bin/fm-pr-host-lib.sh
. "$SCRIPT_DIR/fm-pr-host-lib.sh"
"$FM_ROOT/bin/fm-guard.sh" || true
ID=$1
URL=$2

META="$STATE/$ID.meta"
WT=
if [ -f "$META" ]; then
  WT=$(grep '^worktree=' "$META" | tail -1 | cut -d= -f2- || true)
  PR_HEAD=
  if [ -n "$WT" ] && [ -d "$WT" ]; then
    REMOTE_HEAD=$(fm_pr_view_head "$URL" "$WT" 2>/dev/null || true)
    [ -n "$REMOTE_HEAD" ] && PR_HEAD=$REMOTE_HEAD
  fi
  if ! grep -qxF "pr=$URL" "$META"; then
    echo "pr=$URL" >> "$META"
  fi
  if [ -n "$PR_HEAD" ] && ! grep -qxF "pr_head=$PR_HEAD" "$META"; then
    echo "pr_head=$PR_HEAD" >> "$META"
  fi
fi

cat > "$STATE/$ID.check.sh" <<EOF
# Auto-generated by fm-pr-check.sh. Prints "merged" iff the PR is merged.
export FM_HOME="$FM_HOME" FM_ROOT="$FM_ROOT"
. "$SCRIPT_DIR/fm-pr-host-lib.sh"
state=\$(fm_pr_view_state "$URL" "$WT" 2>/dev/null)
[ "\$state" = "MERGED" ] && echo "merged"
EOF
echo "armed: state/$ID.check.sh polls $URL"
